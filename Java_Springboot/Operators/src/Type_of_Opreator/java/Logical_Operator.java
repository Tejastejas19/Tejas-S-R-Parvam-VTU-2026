package Type_of_Opreator.java;

public class Logical_Operator {
	public static void main(String[] args) {
		
		int a=10,
             b=5;
		System.out.println("\nLogical Operators:");
        System.out.println("a > 5 && b < 10: " + (a > 5 && b < 10));
        System.out.println("a > 5 || b > 10: " + (a > 5 || b > 10));
        System.out.println("!a > b: " + !(a > b));
	}

}
