package Type_of_Opreator.java;

public class Unary_operator {
	public static void main(String[] args) {

        int a = 10;

        System.out.println("Unary Operator");

        // Increment
        a++;
        System.out.println("After Increment: " + a);

        // Decrement
        a--;
        System.out.println("After Decrement: " + a);
    }
}
