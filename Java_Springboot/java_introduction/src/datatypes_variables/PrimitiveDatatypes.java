package datatypes_variables;

public class PrimitiveDatatypes {
	public static void main(String[] arg) {
		
		//	byte
		
		//		byte age;
		//		age =30;
	
	byte age = 30;
	System.out.println("Age is:"+age);
	
//	short
	
	short var1 = 40;
	System.out.println("short:"+var1);
	
//int 
	
	int total=50;
	System.out.println("int:"+total);
	
//long
	
	long var2 =4000;
	System.out.println("long:"+var2);
	
//float
	
	float marks1 = 50;
	System.out.println("float:"+marks1);
	
//double
	
	double marks2 =3.55;
	System.out.println("double:"+marks2);
	
//char
	
	char marks3 =40;
	System.out.println("char:"+marks3);
	
//boolean
	
	boolean marks4 =true;
	System.out.println("boolean:"+marks4);
}
}
